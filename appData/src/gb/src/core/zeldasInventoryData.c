void DrawKeyIndicator(UBYTE keys) BANKED
{
    unsigned char keyPanel[] = {0xc2,0xc2};

    // draw a 0
    keyPanel[0] = 0xb5;

    if (keys) 
    {
        keyPanel[1] = 0xb6;
    } else 
    {
        keyPanel[1] = 0xb5;
    }
    
    set_bkg_tiles(17, 11, 2, 1, keyPanel);
}
